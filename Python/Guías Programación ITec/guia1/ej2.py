#Pedir tres notas, calcular el promedio y mostrarlo.

nota1 = int(input("\n Inserte primer nota: "))
nota2 = int(input("\n Inserte segunda nota: "))
nota3 = int(input("\n Inserte tercera nota: "))

promedio = (nota1 + nota2 + nota3)/3

print("\n --------------- \n El promedio de las tres notas es: " + str(promedio))